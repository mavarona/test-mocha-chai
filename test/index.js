describe('Sum', () => {
    // Sum positive number with positive number must be positive
    it('Sum positive number with positive number must be positive', () => {
        console.log(2 + 2);
        console.log(4);
    });
    // Sum positive number with a minor negaive number must be positive
    it('Sum positive number with a minor negaive number must be positive', () => {
        console.log(3 + (-2));
        console.log(1);
    });
});